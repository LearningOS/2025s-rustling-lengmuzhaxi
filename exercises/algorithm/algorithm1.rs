use std::ptr::NonNull;

#[derive(Debug)]
struct Node<T> {
    val: T,
    next: Option<NonNull<Node<T>>>,
}

impl<T> Node<T> {
    fn new(t: T) -> Node<T> {
        Node { val: t, next: None }
    }
}

#[derive(Debug)]
struct LinkedList<T> {
    length: u32,
    start: Option<NonNull<Node<T>>>,
    end: Option<NonNull<Node<T>>>,
}

impl<T> Default for LinkedList<T> {
    fn default() -> Self {
        Self::new()
    }
}

impl<T> LinkedList<T> {
    pub fn new() -> Self {
        Self {
            length: 0,
            start: None,
            end: None,
        }
    }

    pub fn add(&mut self, obj: T) {
        let mut node = Box::new(Node::new(obj));
        node.next = None;
        let node_ptr = Some(unsafe { NonNull::new_unchecked(Box::into_raw(node)) });
        match self.end {
            None => self.start = node_ptr,
            Some(end_ptr) => unsafe {
                (*end_ptr.as_ptr()).next = node_ptr;
            },
        }
        self.end = node_ptr;
        self.length += 1;
    }

    pub fn get(&mut self, index: i32) -> Option<&T> {
        self.get_ith_node(self.start, index)
    }

    fn get_ith_node(&mut self, node: Option<NonNull<Node<T>>>, index: i32) -> Option<&T> {
        match node {
            None => None,
            Some(next_ptr) => match index {
                0 => Some(unsafe { &(*next_ptr.as_ptr()).val }),
                _ => self.get_ith_node(unsafe { (*next_ptr.as_ptr()).next }, index - 1),
            },
        }
    }

    /// 从链表中取出头部节点，并更新链表状态
    fn pop_front(&mut self) -> Option<NonNull<Node<T>>> {
        self.start.map(|node_ptr| {
            unsafe {
                // 取出 Box 来获得所有权
                let boxed = Box::from_raw(node_ptr.as_ptr());
                // 更新头指针为当前节点的 next
                self.start = boxed.next;
                if self.start.is_none() {
                    self.end = None;
                }
                self.length -= 1;
                // 重新获取裸指针并返回（此时该节点已从链表中移除）
                NonNull::new_unchecked(Box::into_raw(boxed))
            }
        })
    }

    /// 合并两个有序链表，返回一个新的有序链表
    /// 要求 T 必须实现 Ord，用于比较大小
    pub fn merge(mut list_a: LinkedList<T>, mut list_b: LinkedList<T>) -> Self
    where
        T: Ord,
    {
        let mut result = LinkedList::new();

        // 当两个链表中至少有一个非空时循环
        while list_a.start.is_some() || list_b.start.is_some() {
            let next_node = match (list_a.start, list_b.start) {
                (Some(_), Some(_)) => {
                    // 都非空，比较两者头部的值
                    let a_val = unsafe { &(*list_a.start.unwrap().as_ptr()).val };
                    let b_val = unsafe { &(*list_b.start.unwrap().as_ptr()).val };
                    if a_val <= b_val {
                        list_a.pop_front()
                    } else {
                        list_b.pop_front()
                    }
                }
                (Some(_), None) => list_a.pop_front(),
                (None, Some(_)) => list_b.pop_front(),
                (None, None) => None,
            };

            if let Some(node_ptr) = next_node {
                // 在将节点插入到结果链表之前，清空该节点的 next 字段
                unsafe {
                    (*node_ptr.as_ptr()).next = None;
                }
                match result.end {
                    None => result.start = Some(node_ptr),
                    Some(end_ptr) => unsafe {
                        (*end_ptr.as_ptr()).next = Some(node_ptr);
                    },
                }
                result.end = Some(node_ptr);
                result.length += 1;
            }
        }

        result
    }
}

